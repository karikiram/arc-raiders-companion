// Helper functions
export const getItemById = (id: string): Item | undefined => ITEMS[id];

export const getItemsByCategory = (category: ItemCategory): Item[] =>
  Object.values(ITEMS).filter((item) => item.category === category);

export const getItemsByRarity = (rarity: Rarity): Item[] =>
  Object.values(ITEMS).filter((item) => item.rarity === rarity);

export const getAllItems = (): Item[] => Object.values(ITEMS);

export const searchItems = (query: string): Item[] => {
  const lowerQuery = query.toLowerCase();
  return Object.values(ITEMS).filter(
    (item) =>
      item.name.toLowerCase().includes(lowerQuery) ||
      item.description.toLowerCase().includes(lowerQuery)
  );
};
