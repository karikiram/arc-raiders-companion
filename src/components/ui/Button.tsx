'use client';

import { forwardRef, type ButtonHTMLAttributes } from 'react';
import { cn } from '@/lib/utils';

export interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'default' | 'primary' | 'secondary' | 'ghost' | 'danger';
  size?: 'sm' | 'md' | 'lg';
}

const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant = 'default', size = 'md', children, ...props }, ref) => {
    return (
      <button
        ref={ref}
        className={cn(
          'inline-flex items-center justify-center rounded-lg font-medium transition-colors',
          'focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-offset-background',
          'disabled:opacity-50 disabled:pointer-events-none',
          {
            'bg-zinc-800 text-white hover:bg-zinc-700 focus:ring-zinc-500':
              variant === 'default',
            'bg-accent text-black hover:bg-accent-light focus:ring-accent':
              variant === 'primary',
            'bg-zinc-700 text-zinc-100 hover:bg-zinc-600 focus:ring-zinc-500':
              variant === 'secondary',
            'bg-transparent text-zinc-400 hover:text-white hover:bg-zinc-800 focus:ring-zinc-500':
              variant === 'ghost',
            'bg-red-600 text-white hover:bg-red-500 focus:ring-red-500':
              variant === 'danger',
          },
          {
            'px-3 py-1.5 text-sm': size === 'sm',
            'px-4 py-2 text-sm': size === 'md',
            'px-6 py-3 text-base': size === 'lg',
          },
          className
        )}
        {...props}
      >
        {children}
      </button>
    );
  }
);

Button.displayName = 'Button';

export { Button };
